# Ejercicio 11: Calculadora de edad.
# Escribe un programa que solicite al ususario su año de nacimiento y calcule su edad actual.

year_actual = 2024
year_nacimiento = int(input("Introduzca tu año de nacimiento: "))
edad = year_actual - year_nacimiento 

print(f"Si tú año de nacimiento es {year_nacimiento } tu edad actual es {edad}",)
